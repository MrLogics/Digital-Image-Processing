img = imread('cameraman.tif');
f2 = fftshift(fft2(img));
figure;
imshow(0.5*log(1+abs(f2)), []);
x = 3;
d = 15;
[m, n] = size(img);
[p, q] = meshgrid(-floor(m/2):floor((m-1)/2), -floor(n/2):floor((n-1)/2));
D = sqrt(p.^2 + q.^2);
C = 1./(1+(D./d).^(2*x));
lp = f2.*C;
lpi = ifft2(ifftshift(lp));
f = abs(lpi);
fm = max(f(:));
figure;
imshow(f/fm, []);